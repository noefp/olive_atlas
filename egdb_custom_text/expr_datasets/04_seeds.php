<p>
  Olive fruits were collected 210 days after anthesis from adult olive trees growing at 
  the Estación Experimental del Zaidín in Granada, Spain. 
  One biological replicate of Seed and three biological replicates of Testa+Endosperm and Embryo were sequenced.
  Data shown were normalized to TPM.
</p>
<p>
  <b>Note:</b> This dataset is unpublished and the manuscript is in preparation. The gene expression data of endosperm and embryo samples will be released after publication.
</p>
