  combined from several experiments.
</p>
<h4>Experimental Conditions (all in control conditions with 2 replicates unless otherwise noted):</h4>

<ul>
  <li>Roots.</li>
  <li><span style="color:green;">Roots 4 months</span>.</li>
  <li>Stem.</li>
  <li>Meristem.</li>
  <li>Leaves.</li>
  <li><span style="color:green;">Leaves 4 months</span>.</li>
  <li>Flowers: Recently bloomed whole flowers (at anthesis).</li>
  <li><span style="color:orange;">Pollen mature</span>: 3 replicates of mature pollen.</li>
	<li><span style="color:orange;">Pollen germ 6h</span>: 3 replicates of pollen after 6 hours of germination.</li>
  <li>Fruits: Reaching the turning ripening state, veraison.</li>
  <li><span style="color:blue;">Seed</span>: Mature seed (whole), 1 replicate.</li>
  <!-- <li><span style="color:blue;">Testa+endosperm.</span> Endosperm and seed coat (testa), 3 replicates.</li>
  <li><span style="color:blue;">Embryo.</span> Mature embryo, 3 replicates.</li> -->
</ul>


