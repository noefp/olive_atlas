<p>
  RNA-seq of roots, stem, meristem, leaves, flowers, pollen, fruits (epicarp + mesocarp), seeds, embryo, and testa+endosperm,
  combined from several experiments.
</p>
<h4>Experimental Conditions (all in control conditions with 2 replicates unless otherwise noted):</h4>
  <ul>
    <li>Roots.</li>
    <li><span style="color:green;">Roots 4 month.</span></li>
    <li>Stem.</li>
    <li>Meristem.</li>
    <li>Leaves.</li>
    <li><span style="color:green;">Leaves 4 month.</span></li>
    <li>Flowers.</li>
    <li><span style="color:orange;">Pollen.</span> 3 replicates of mature pollen.</li>
    <li>Fruits.</li>
    <li><span style="color:blue;">Seed.</span> Only 1 replicate.</li>
    <li><span style="color:blue;">Testa+endosperm.</span> 3 replicates.</li>
    <li><span style="color:blue;">Embryo.</span> 3 replicates.</li>
  </ul>
<p>
  <span style="color:green;">Leaves and Roots</span> (in green) are control samples from 4 month old potted plants in conditions as published by <a href="https://doi.org/10.1093/dnares/dsu033" target="_blank">Leyva-Pérez et al., 2015</a>.
  <span style="color:blue;">Seed, Embryo and Testa+endosperm</span> (in blue) are control samples in conditions as published by <a href="#" target="_blank">Bullones et al., 2022. In preparation</a>.
  <span style="color:orange;">Pollen</span> samples correspond to mature pollen in conditions as published by <a href="#" target="_blank">Bullones et al., 2022. In preparation</a>.
  The rest of the tissues (in black) come from three healthy 10-year-old olive trees under field conditions. They were published by <a href="https://doi.org/10.3390/genes11050544" target="_blank">Ramírez-Tejero et al., 2020</a>.
</p>
<p>
  All samples were normalized to TPM.
</p>


