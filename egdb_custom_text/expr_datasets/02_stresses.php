<p>
  RNA-seq of leaves and roots from 4 month old potted olive plants in control and stess conditions.
  Abiotic stresses consisted of wounded roots and low temperatures, and biotic estress of infection with the fungal plant pathogen <i>Verticillium dahliae</i>.
</p>
<p>
  Verticillium infection was performed by root-dip inoculation in a conidia suspension of a <i>Verticillium dahliae</i> isolate representative of the so-called defoliating pathotype.
  Cold stress conditions were 10°C during the day and 4°C in the night, with 14 hours photoperiod.
</p>

