<p>
  RNA-seq of leaves and roots from 4 month old potted olive plants in control conditions and 
  infected with the fungal plant pathogen <i>Verticillium dahliae</i>. 
  The infection was performed by root-dip inoculation in a conidia suspension of a <i>Verticillium dahliae</i> isolate representative of the so-called defoliating pathotype.
</p>

<h4>Experimental Conditions (2 biological replicates):</h4>
<ul>
  <li>Leaves control.</li>
  <li>Leaves Verticillium 15d. Leaves infected with Verticillium for 15 days.</li>
  <li>Roots control.</li>
  <li>Roots Verticillium 48h. Roots infected with Verticillium for 48 hours.</li>
  <li>Roots Verticillium 7d. Roots infected with Verticillium for 7 days.</li>
  <li>Roots Verticillium 15d. Roots infected with Verticillium for 15 days.</li>
</ul>

