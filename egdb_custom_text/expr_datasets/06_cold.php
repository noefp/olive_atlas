<p style="width:1000px">
  (A) Control plant; 
  (B) cold-stressed plant during 1 day; 
  (C) cold-stressed plant during 2 days; 
  (D) cold-stressed plant during 3 days; 
  (E) almost fully recovered plant after 6 days of cold stress; 
  (F) fully recovered plant after 10 days of cold stress. Plants show flaccid leaved and stresed phenotype in B and is progessively recovering from D to F.
</p>

<p>
  The plant samples employed for this study were obtained from a commercial nursery located in Córdoba province, Spain.
  Two biological replicates per sample were sequenced.
  cDNA libraries were sequenced by PE sequencing (100 x 2) with an Illumina HiSeq 1000 sequencer and normalized to TPM.
</p>
<p>
  This dataset was published by <a href="https://doi.org/10.1093/dnares/dsu033" target="_blank">Leyva-Pérez et al. 2014</a> 
  and raw data can be found in the <a href="https://www.ncbi.nlm.nih.gov/bioproject/PRJNA256033" target="_blank">BioProject PRJNA256033</a>.
</p>


