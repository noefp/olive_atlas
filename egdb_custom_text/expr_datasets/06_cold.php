<p>
  RNA-seq of leaves from 4 month old potted olive plants in control and low temperature stess conditions, 
  with 10°C during the day and 4°C in the night, with 14 hours photoperiod.
</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Leaves control.</li>
  <li>Leaves cold 24h. Leaves after 24 hours in cold conditions.</li>
  <li>Leaves cold 10d. Leaves after 10 days in cold conditions.</li>
</ul>


<p>
  The plant samples employed for this study were obtained from The plants were obtained from a commercial nursery located in Córdoba province, Spain.
  Two biological replicates per sample were sequenced.
  cDNA libraries were sequenced by PE sequencing (100 x 2) with an Illumina HiSeq 1000 sequencer and normalized to TPM.
</p>
<p>
  This dataset was published by <a href="https://doi.org/10.1093/dnares/dsu033" target="_blank">Leyva-Pérez et al. 2014</a> 
  and raw data can be found in the <a href="https://www.ncbi.nlm.nih.gov/bioproject/PRJNA256033" target="_blank">BioProject PRJNA256033</a>.
</p>


