<p>
  RNA-seq of pollen tube growth at four times of development. 
  Pollen grains were pre-hydratated by incubation in a humid chamber at 
  room temperature for 1 hour and then transferred to Petri dishes containing germination medium. 
  Petri dishes were maintained at room temperature in the dark and under gentle agitation. 
  Pollen grains were sampled at 1, 3 and 6 hours after transfer to the in vitro germination medium.
</p>
<h4>Experimental Conditions:</h4>

<ul>
  <li>Pollen. Mature pollen.</li>
  <li>Pollen germ 1h. Pollen after 1 hour of germination.</li>
  <li>Pollen germ 3h. Pollen after 3 hours of germination.</li>
  <li>Pollen germ 6h. Pollen after 6 hours of germination.</li>
</ul>
  
