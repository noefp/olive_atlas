<p>
  RNA-seq of roots, stems, meristems, leaves, flowers, and fruits (epicarp and mesocarp)
  of three healthy 10-year-old olive trees under field conditions. All the samples were collected at the same time,
  except for fruits, which had to be collected later upon reaching the turning ripening state. 
  <br>
  <br>
  The plant samples employed for this study were obtained from the World Olive Germplasm Collection (WOGC) 
  of the Andalusian Institute of Agricultural and Fisheries Research and Training (IFAPA).
  cDNA libraries were sequenced by PE sequencing (100 x 2) with an Illumina HiSeq 2000 sequencer and normalized to TPM.
